<?php

namespace App\Repositories;

use PDO;

/**
 * Базовый класс репозитория
 * 
 * Предоставляет общую функциональность для всех репозиториев:
 * - подключение к базе данных через PDO
 * 
 * Репозитории отвечают за работу с данными в базе данных:
 * - выполнение SQL запросов
 * - преобразование результатов запросов в массивы
 * - не содержат бизнес-логику (она находится в Services)
 */
abstract class Repository
{
    /**
     * @var PDO Подключение к базе данных
     */
    protected PDO $pdo;

    /**
     * Конструктор репозитория
     * 
     * @param PDO $pdo Подключение к базе данных
     */
    public function __construct(PDO $pdo)
    {
        $this->pdo = $pdo;
    }
}
